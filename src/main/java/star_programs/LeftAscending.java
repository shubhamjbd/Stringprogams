package star_programs;

public class LeftAscending {

	public static void main(String[] args) {
		//5*5
		
		
		
		int star=1;
		for(int i=star;i<=5;i++)
		{
			for(int j=1;j<=star;j++)
			{
				System.out.print("*");
				
				
				 
			}
			System.out.println();
			star++;
			
			
		}
		
		
 
	}

}
